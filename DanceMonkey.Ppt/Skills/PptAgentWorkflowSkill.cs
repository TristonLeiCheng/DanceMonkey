/// 直接以常量形式内置，避免依赖磁盘 SKILL.md。
/// </summary>
internal static class PptAgentWorkflowSkill
{
    public const string Markdown = """
        # PPT 分阶段工作流（内置技能）

        在生成「演示文稿大纲 JSON」时，请把这件事当作分阶段工作，而不是一次性堆字。

        ## 阶段一：理解来源

        先在心里梳理以下三个问题，再动笔：
        1. **核心信息**：这篇内容讲了什么？最重要的 1~3 个事实/结论是什么？
        2. **受众需求**：对方最关心的是什么？他们的知识背景如何？
        3. **演示目的**：是让人知道（informational）、让人认可（persuasive），还是让人学会（training）？

        ## 阶段二：规划叙事弧（Coherence 第一）

        高质量演示必须有完整故事线，参考下列模式之一：

        - **问题-方案模式**：背景现状 → 核心问题 → 解决方案 → 效果/证据 → 行动号召
        - **数据洞察模式**：现状数据 → 趋势发现 → 原因分析 → 建议方向 → 结论
        - **教学模式**：概念引入 → 核心原理 → 案例演示 → 常见误区 → 总结练习

        每章节应承担叙事弧中的一个清晰角色；章节之间须有逻辑过渡。

        ## 阶段三：页面规划与版式选择

        - 每章节 2~6 页，每页负责「一个可独立讲解的小观点」。
        - **版式选择规则**（优先级从高到低）：
          1. 有对立/并列两组内容（如优缺点、前后对比）→ `twoColumn`
          2. 有高影响力金句、单一数据亮点或名言 → `quote`（正文放 bullets[0]，来源放 subtitle）
          3. 有数字对比/趋势 → `bullets` + 填写 `visualSuggestion`（建议图表类型）
          4. 其余 → `bullets`

        ## 阶段四：内容质量控制

        - **keyMessage**：每页必须有一句「核心结论句」（≤30字），是去掉 bullets 后观众唯一能带走的信息。
          例：❌「产品特性比较」  ✅「A 产品成本降低 40%，交付时间缩短 2 周」
        - **bullets**：2~5 条，支撑 keyMessage；每条独立可读；避免重复标题信息。
        - **speakerNotes**：2~4 句口语化提示，补充 bullets 背后的逻辑或故事，不要重复 bullets。
        - **visualSuggestion**：只在有数字/趋势/对比时填，一句话说明图表类型与数据维度。

        ## 阶段五：信息纪律

        - 忠实来源；不补造数据；遇到模糊处宁可省略。
        - 宁可少出页，不要为凑数把一条要点拆成两条。
        - 若来源只提供主题词，可合理推断结构，但须在 speakerNotes 中标注「（待补充）」。

        ## 反模式（必须主动规避）

        | 反模式 | 说明 |
        |--------|------|
        | 文字墙 | 单页 bullets 超过 6 条，或每条超过 80 字 |
        | 标题冗长 | 标题超过 18 字，无法在 3 秒内读完 |
        | 版式单调 | 连续 5 页以上使用同一版式 |
        | 结论缺失 | 有数据没结论，keyMessage 只描述现象不给判断 |
        | 注释误用 | 把「待补充」「TODO」写进 bullets 而不是 speakerNotes |

        ## 输出契约

        - **只输出**一个 JSON 对象，符合系统提示中的 schema；不要 Markdown 围栏，不要解释。
        - 字段缺失时省略，不要写 null。
        - 不要新增 schema 没有的字段。
        """;
}
